public class InsertionSort {

    void sort(double[] array, int n) {
        int k;
        for (int i = 1; i < n; i++) {
            double temp = array[i];
            int j = i - 1;
            while (j >= 0 && temp < array[j]) {
                array[j + 1] = array[j];
                j--;
            }
            array[j + 1] = temp;
        }
    }
}
